    private void prepare() {
        FileInputStream in = null;
        try {
            in = new FileInputStream("config/dbconfig.properties");
            Properties props = new Properties();
            props.load(in);
            txtPassword.setText(props.getProperty("password"));
            txtURL.setText(props.getProperty("url"));
            txtUsername.setText(props.getProperty("username"));
            in.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(FrmConfig.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(FrmConfig.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                in.close();
            } catch (IOException ex) {
                Logger.getLogger(FrmConfig.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
