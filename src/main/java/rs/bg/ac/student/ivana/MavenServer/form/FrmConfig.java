    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String url = txtURL.getText().trim();
        String password = txtPassword.getText().trim();
        String username = txtUsername.getText().trim();
        
        FileOutputStream out = null;
        try{
            out = new FileOutputStream("config/dbconfig.properties");
            FileInputStream in = new FileInputStream("config/dbconfig.properties");
            Properties props = new Properties();
            props.load(in);
            in.close();

            props.setProperty("url", url);
            props.setProperty("username", username);
            props.setProperty("password", password);
            props.store(out, null);
            out.close();
            JOptionPane.showMessageDialog(this, "Database configuration added succeffully!");
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error: "+e.getMessage());
            try {
                out.close();
            } catch (IOException ex) {
                Logger.getLogger(FrmConfig.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

